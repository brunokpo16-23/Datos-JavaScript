resultado = (20 + 30) * 2; // IGUAL A 100

resultado = (200 + 100 + 300 + 1000) * .2; // Esto nos dara el porcentaje que se le restara, es util para un descuento en una tienda.

// Incrementos 
let puntaje = 10;
// Incrementos en 1 con ++
puntaje++; // Este seria 10 porque primero lo muestra y despues lo suma 
++puntaje; // Este seria 12 porque primero lo esta sumando y luego lo muestra 

// Restar puntos
puntaje--; // Este seria 10 porque primero lo muestra y despues lo suma 
--puntaje; // Este seria 8 porque primero lo esta sumando y luego lo muestra 

// Incrementos en mas de 1

console.log(puntaje);

puntaje += 10; // Este le sumara 10 puntos a nuestro numero principal
puntaje -= 10; // Este le restara 10 puntos a nuestro numero principal

